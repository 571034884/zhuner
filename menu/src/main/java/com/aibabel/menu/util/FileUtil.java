    public static long sizeFormatString2Num(String str){

        str=str.replace(" ","");
        long size = 0;
        try {
            if(str!=null){
                if(str.endsWith("KB"))
                    size = (long)(Double.parseDouble(str.substring(0,str.length()-2))*1024);
                else if(str.endsWith("MB"))
                    size = (long)(Double.parseDouble(str.substring(0,str.length()-2))*1024*1024);

                else if(str.endsWith("GB"))
                    size = (long)(Double.parseDouble(str.substring(0,str.length()-2))*1024*1024*1024);
            }
        } catch (Exception e) {
            L.e("sizeFormatString2Num======"+e.getMessage());
        }

        return size;

    }



    public  static void saveImage(Context mContext,Bitmap bmp,String fileName) {

        File appDir = new File(mContext.getFilesDir().getAbsolutePath());
        if (!appDir.exists()) {
            appDir.mkdir();
        }
        String currfileName = fileName + ".jpg";
        File file = new File(appDir, currfileName);
        try {
            FileOutputStream fos = new FileOutputStream(file);
            bmp.compress(Bitmap.CompressFormat.JPEG, 100, fos);
            fos.flush();
            fos.close();
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

